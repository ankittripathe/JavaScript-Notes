// (1) Pre-increment (++a) => Pre-increment operator, which means that the value of a is increased before it is used in the expression.

// Example.1
let a = 5;
console.log('a =', a)     // output: a = 5

console.log('a =', ++a);  // output: ++a = 6 (Pahle increment kr lega tab print hoga)

console.log('a =', a);   // output: a = 6 (finally updated value printed here)


// (2) Pre-decrement (--x) => Pre-decrement operator, which means that the value of a is decreased before it is used in the expression.

// Example.1
let x = 33;
console.log('x =', x);    // output: x = 33

console.log('x =', --x);  // output: --x = 32  (pahle decrement kr lega tab print hoga)

console.log('x =', x);  // output: x = 32
