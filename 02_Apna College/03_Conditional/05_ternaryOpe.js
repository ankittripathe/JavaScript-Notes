/*  Ternary Operator Syntax:-
    condition ? 'true will Executed' : 'false will Executed'  
*/

// Example.1
let age = 3;
let result = age >= 18 ? 'You are Adult' : 'You are Kid'
console.log(result);  // output:- You are kid


// Exmaple.1 (2nd method)
let ages = 14;
ages >= 18 ? console.log('you are Adult') : console.log('you are Kid');
// output:- you are kid


// Example.2
let marks = 55;
let final = marks>=32 ? 'Passed' : "Failed"
console.log(final); // output:- Passed


// Example.3
let score = 100;
score >=100 ? console.log('Century') : console.log('Better luck next time');
