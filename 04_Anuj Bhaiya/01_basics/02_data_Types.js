//! Data Types  //
// (1) number
let a = 7;
console.log(a);
console.log("The value of a =", a);      // using double quotes
console.log('The value of a =', a);      // using single quotes
console.log(`The value of a = ${a}`);    // using backticks

console.log(typeof a);
console.log("The types of a =", typeof (a));  // Both method are right. 


// (2) string
let name = 'Vickey'
console.log(name);
console.log(typeof name);

// (3) boolean
let c = true 
console.log(c);
console.log(typeof c);