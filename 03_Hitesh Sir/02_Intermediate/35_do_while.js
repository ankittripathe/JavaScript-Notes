/* do while loop => Executed atleast once either condition is true or false.

    do {

   }
   while(condition)

*/

// Example.1
let score = 11             // do-while => Executed atleast once either condition is true or false.
do {
   console.log(`Score = ${score}`);
   score++
} while(score <= 10)


// Example.2
let marks = 2
do {
   console.log(`your marks = ${marks}`);
   marks++
} while(marks <= 10)

